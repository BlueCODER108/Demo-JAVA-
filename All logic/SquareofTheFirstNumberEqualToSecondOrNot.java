import java.util.Scanner;
class SquareofTheFirstNumberEqualToSecondOrNot
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter a number: ");
		double a= sc.nextDouble();

		System.out.println("Enter a number: ");
		double b = sc.nextDouble();

		if (a*a == b)
		  System.out.println("The Square of the First Number is equal to the Second");
		else
			System.out.println("The Square of the First Number is NOT equal to the Second");
	}
}
